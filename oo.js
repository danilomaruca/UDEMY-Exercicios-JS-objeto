// 1. abstração = traduzir um objeto do mundo real para dentro do sistema
// 2. encapsulamento = informações de implementação escondidos e mostrar numa interface simples para o usuário
// 3. herança (prototype) = caractecristicas/atributos/comportamentos herdados de uma estrutura "pai"
// 4. polimorfismo = multiplas formas, tipos (objetos) concretos a partir de uma variável genérica 