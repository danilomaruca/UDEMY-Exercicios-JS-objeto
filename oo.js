//  Procedural 
processamento(valor1, valor2, valor3)       // função que manipula dados/objetos

// OO (objetos compostos por dados e funções)
objeto = {          // dados persolanizados com atributos e comportamentos 
    valor1, 
    valor2,
    valor3,
    processamento() {
        // ...
    }
}

objeto.processamento() // Foco passou a ser o objeto

